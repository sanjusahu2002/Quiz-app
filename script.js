// sections
let sectionDiv = document.getElementById("section-div");
let questionContainer = document.getElementById("question-container");
let questionsbtn = document.getElementById('questions-btn');
let fillTypeQuestion = document.getElementById('fill-type-question');
let numTypeQuestion = document.getElementById('num-type-question');


// buttons
let gnPlay = document.getElementById('gn-play');
