let nextBtn = document.getElementById('next-btn');


// required variables
let score = 0;
// nextBtn.addEventListener('click',);

function resetQuiz(){
    questionsbtn.innerHTML  = '';
    fillTypeQuestion.style.display = 'none'
    numTypeQuestion.style.display= 'none';
    
}


function startQuiz(index){
    resetQuiz();
    score =0;

    let currentQuestionSection = questionObject.sections[index];
    let questionArray = currentQuestionSection.questions;
    let count =0;
    let answerSelected = false;
    

    sectionDiv.style.display = 'none';
    questionContainer.style.display = 'block';
    
    showQuestion();

    function showQuestion(){
      resetQuiz();
      let question = questionArray[count].question;
      document.getElementById('question').innerHTML = question;
      let questionType = questionArray[count].questionType;
      console.log(count)
      if(questionType === 'mcq'){
        let options = questionArray[count].options;
        console.log(options);
        options.forEach((elm)=>{
          let button = document.createElement('button');
          button.innerHTML = elm;
          questionsbtn.style.display = 'block';
          questionsbtn.appendChild(button);
          button.addEventListener('click',function(){
            if(!answerSelected){
              answerSelected = true;
              console.log(button)
              button.classList.add('selected');
              console.log('correct',questionArray[count].answer,'user',elm)
              checkAnswer(elm,questionArray[count].answer);
              }
            });
      
          })
          }
      if(questionType === 'text'){
        fillTypeQuestion.style.display = 'block';
        let fillElement = document.getElementById('answer');
        fillElement.value = '';
        let submitFill = document.getElementById('submit-answer');
        submitFill.addEventListener('click',()=>{
          if(!answerSelected){
              answerSelected = true;
              checkAnswer(fillElement.value.toString(),questionArray[count].answer.toString());
                
            }
        });
      
      }
      if(questionType === 'number'){
          numTypeQuestion.style.display = 'block';
          let numElement = document.getElementById('num');
          numElement.value = '';
          let numSubmit = document.getElementById('num-answer');
          numSubmit.addEventListener('click',()=>{
          if(!answerSelected){
            answerSelected = true;
              checkAnswer(numElement.value.toString(), questionArray[count].answer.toString());
          
            }
          })
      }
  
     
  }
    nextBtn.addEventListener('click',nextQuestion);

    function nextQuestion(){
      if(count < questionArray.length){
        count++;
        if(count<questionArray.length){
          answerSelected = false;
          let feedbackElement = document.getElementById('f-div');
          feedbackElement.innerHTML = '';
          showQuestion();
        }
        else{
          console.log("complete")
          questionsbtn.innerHTML  = '';
          fillTypeQuestion.style.display = 'none'
          numTypeQuestion.style.display= 'none';
          let marks = document.getElementById('question');
          marks.innerHTML  = `Quiz ended , you scored ${score} out of ${questionArray.length} and Incorrect/Unattempt : ${questionArray.length - score}`;
          nextBtn.innerHTML = "Go To Home";
          document.getElementById('f-div').innerHTML = '';

        }
      }
      else{
        sectionDiv.style.display = 'grid';
        questionContainer.style.display = 'none';
        nextBtn.innerHTML = 'Next';
        count= 0;
        score = 0;
        document.getElementById('score').innerHTML = `Score : ${score}`;
        
      } 
    }

}


function checkAnswer(userAnswer,questionAnswer){
  let feedbackElement = document.getElementById('f-div');

  if(userAnswer === questionAnswer || userAnswer.toLowerCase() === questionAnswer.toLowerCase()){
    score++;
    feedbackElement.innerHTML = 'Correct';
    feedbackElement.style.color = 'green';
  }
  else{
    if(userAnswer.length === 0){
      feedbackElement.innerHTML = `Don't know don't worry correct is ${questionAnswer}`;
    }
    else{
      feedbackElement.innerHTML = `Wrong Answer correct is ${questionAnswer}`;
    }
    feedbackElement.style.color = 'red';
  }
  updateScore();

}
function updateScore(){
  let scoreElement = document.getElementById('score');
  scoreElement.innerHTML = `Score : ${score}`;
}






